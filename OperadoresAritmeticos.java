public class OperadoresAritmeticos { 
    public static void main(String[] args) {

       int minhaIdade = 40;
       int suaIdade = 25;
        // Adição
       int totalIdades = minhaIdade + suaIdade;
       System.out.println("A soma de Total das idades é: " + totalIdades);
       
       // Subtração
       int subtracaoIdades = minhaIdade - suaIdade;
       System.out.println("A divisão de minhaIdade e a suaIdade é: " + subtracaoIdades);
       
       // Multiplicação
       int multiplicacaoIdades = minhaIdade * suaIdade;
       System.out.println("A Multiplicação da minhaIdade * suaIdade é: " + multiplicacaoIdades);
       
       // Divisão
       int divisaoIdades = minhaIdade / suaIdade;
       
       System.out.println("A Divisão da minhaIdade / suaIdade é: " + divisaoIdades);

    }


}